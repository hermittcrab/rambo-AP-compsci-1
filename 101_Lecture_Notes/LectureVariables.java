/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        String name = new String("rambo");
        int x = 15;
        double y = 13.4;
        
        System.out.println(name + " stink so much.");
        System.out.println(name + " has " + x + " teeth left.");
        System.out.println(name + " owes me " + y + " dollars.");
        
	}
}